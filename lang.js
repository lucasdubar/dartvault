  window.DV_LANG = {
    get: function () {
      return localStorage.getItem('dartvault_lang') || 'en';
    },
    t: function (key) {
      var lang = window.DV_LANG.get();
      // Fallback: DE/NL/ES → EN → FR
      if (T[lang] && T[lang][key] !== undefined) return T[lang][key];
      if ((lang === 'de' || lang === 'nl' || lang === 'es') && T['en'] && T['en'][key] !== undefined) return T['en'][key];
      return T['fr'][key] || key;
    },
    apply: function () {
      var lang = window.DV_LANG.get();
      document.querySelectorAll('[data-i18n]').forEach(function (el) {
        var key = el.dataset.i18n;
        var val = window.DV_LANG.t(key);
        if (val !== undefined) el.textContent = val;
      });
      document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
        var key = el.dataset.i18nHtml;
        var val = window.DV_LANG.t(key);
        if (val !== undefined) el.textContent = val;
      });
      // Placeholder inputs
      document.querySelectorAll('[data-i18n-ph]').forEach(function (el) {
        el.placeholder = window.DV_LANG.t(el.dataset.i18nPh);
      });
    },
    showPicker: function (btn) {
      var existing = document.getElementById('dv-lang-picker');
      if (existing) { existing.remove(); return; }
      var langs = [
        { code: 'fr', flag: '🇫🇷', label: 'Français' },
        { code: 'en', flag: '🇬🇧', label: 'English' },
        { code: 'de', flag: '🇩🇪', label: 'Deutsch' },
        { code: 'nl', flag: '🇳🇱', label: 'Nederlands' },
        { code: 'es', flag: '🇪🇸', label: 'Español' }
      ];
      var isDark = !document.documentElement.getAttribute('data-theme') || document.documentElement.getAttribute('data-theme') !== 'light';
      var picker = document.createElement('div');
      picker.id = 'dv-lang-picker';
      picker.style.cssText = 'position:fixed;z-index:99999;background:' + (isDark ? '#1C1E20' : '#F7F0F2') + ';border:1px solid rgba(128,128,128,.25);border-radius:12px;padding:4px;display:flex;flex-direction:column;gap:2px;box-shadow:0 4px 24px rgba(0,0,0,.55);min-width:140px;';
      var rect = btn.getBoundingClientRect();
      var top = rect.bottom + 6;
      var right = window.innerWidth - rect.right;
      if (top + 130 > window.innerHeight) top = rect.top - 130 - 6;
      picker.style.top = top + 'px';
      picker.style.right = right + 'px';
      var cur = window.DV_LANG.get();
      langs.forEach(function (l) {
        var item = document.createElement('button');
        item.style.cssText = 'background:' + (l.code === cur ? (isDark ? 'rgba(255,255,255,.1)' : 'rgba(0,0,0,.08)') : 'none') + ';border:none;color:' + (isDark ? '#F0F0F0' : '#111') + ';border-radius:8px;padding:9px 14px;cursor:pointer;font-size:.95rem;font-family:\'Exo 2\',sans-serif;font-weight:600;display:flex;align-items:center;gap:10px;white-space:nowrap;touch-action:manipulation;width:100%;text-align:left;';
        item.innerHTML = '<span style="font-size:1.3rem">' + l.flag + '</span><span>' + l.label + '</span>';
        item.addEventListener('click', function (e) {
          e.stopPropagation();
          localStorage.setItem('dartvault_lang', l.code);
          picker.remove();
          document.removeEventListener('click', outsideClick, true);
          window.DV_LANG.apply();
          window.dispatchEvent(new CustomEvent('dv-lang-change', { detail: { lang: l.code } }));
        });
        picker.appendChild(item);
      });
      document.body.appendChild(picker);
      function outsideClick(e) {
        if (!picker.contains(e.target) && e.target !== btn) {
          picker.remove();
          document.removeEventListener('click', outsideClick, true);
        }
      }
      setTimeout(function () {
        document.addEventListener('click', outsideClick, true);
      }, 50);
    }
  };
